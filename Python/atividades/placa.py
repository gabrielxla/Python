placa = int (input("Digite o final da sua placa: "))

if placa == 1 or placa == 2:
    print("Não roda na segunda")
elif placa == 3 or placa == 4:
    print("Não roda na terça")
elif placa == 5 or placa == 6:
    print("Não roda na quarta")
elif placa == 7 or placa == 8:
    print("Não roda na quinta")
elif placa == 9 or placa == 0:
    print("Não roda na sexta")
else:
    print("invalida")